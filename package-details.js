// Initialize details functionality when DOM is loaded
document.addEventListener('DOMContentLoaded', function() {
    initPackageDetails();
});

// Function to initialize package details functionality
function initPackageDetails() {
    const detailsButtons = document.querySelectorAll('.details-btn');
    const packageDetailsFullpage = document.querySelector('.package-details-fullpage');
    const backToPackages = document.querySelector('.back-to-packages');
    const bookNowFixed = document.querySelector('.book-now-fixed');
    
    // Open details view when clicking details button
    detailsButtons.forEach(button => {
        button.addEventListener('click', function(e) {
            e.preventDefault();
            const packageId = this.getAttribute('data-package-id');
            showPackageDetails(packageId);
        });
    });
    
    // Go back to packages listing
    if (backToPackages) {
        backToPackages.addEventListener('click', function() {
            packageDetailsFullpage.style.display = 'none';
            document.body.style.overflow = 'auto'; // Enable scrolling on the main page
        });
    }
    
    // Handle book now click from details page
    if (bookNowFixed) {
        bookNowFixed.addEventListener('click', function(e) {
            e.preventDefault();
            const packageName = this.getAttribute('data-package');
            // Close details view
            packageDetailsFullpage.style.display = 'none';
            document.body.style.overflow = 'auto';
            
            // Open booking modal
            const modal = document.getElementById('booking-modal');
            const bookingTitle = document.getElementById('booking-title');
            bookingTitle.textContent = `Book Your ${packageName} Package`;
            modal.style.display = 'flex';
        });
    }
}

// Function to display package details
function showPackageDetails(packageId) {
    const packageDetailsFullpage = document.querySelector('.package-details-fullpage');
    const bookNowFixed = document.querySelector('.book-now-fixed');
    
    if (packageDetails[packageId]) {
        const details = packageDetails[packageId];
        
        // Update header
        const header = packageDetailsFullpage.querySelector('.package-details-header');
        header.style.backgroundImage = `url('${details.image}')`;
        
        // Update title and price
        packageDetailsFullpage.querySelector('.package-details-title h2').textContent = details.title;
        packageDetailsFullpage.querySelector('.price').textContent = details.price;
        
        // Update info grid
        packageDetailsFullpage.querySelector('.duration-value').textContent = details.duration;
        packageDetailsFullpage.querySelector('.group-value').textContent = details.groupSize;
        packageDetailsFullpage.querySelector('.accommodation-value').textContent = details.accommodation;
        packageDetailsFullpage.querySelector('.meals-value').textContent = details.meals;
        
        // Update description
        packageDetailsFullpage.querySelector('.description-text').textContent = details.description;
        
        // Update itinerary
        const itineraryContainer = packageDetailsFullpage.querySelector('.itinerary-container');
        itineraryContainer.innerHTML = '';
        details.itinerary.forEach(day => {
            const dayElement = document.createElement('div');
            dayElement.className = 'itinerary-day';
            dayElement.innerHTML = `
                <h4 class="day-title">${day.day}</h4>
                <p>${day.activities}</p>
            `;
            itineraryContainer.appendChild(dayElement);
        });
        
        // Update included/not included lists
        const includedList = packageDetailsFullpage.querySelector('.included-list');
        includedList.innerHTML = '';
        details.included.forEach(item => {
            const li = document.createElement('li');
            li.textContent = item;
            includedList.appendChild(li);
        });
        
        const notIncludedList = packageDetailsFullpage.querySelector('.not-included-list');
        notIncludedList.innerHTML = '';
        details.notIncluded.forEach(item => {
            const li = document.createElement('li');
            li.textContent = item;
            notIncludedList.appendChild(li);
        });
        
        // Update the book now button with the package name
        bookNowFixed.setAttribute('data-package', details.title);
        
        // Show the details page
        packageDetailsFullpage.style.display = 'block';
        document.body.style.overflow = 'hidden'; // Prevent scrolling on the main page
        
        // Scroll to top of details page
        packageDetailsFullpage.scrollTop = 0;
    }
}

// Export functions to be used globally
window.showPackageDetails = showPackageDetails;
window.initPackageDetails = initPackageDetails;
